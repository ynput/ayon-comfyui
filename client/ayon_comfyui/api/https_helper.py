"""Tools for https on localhost.

Since companies may want to host ComfyUI over TLS (https),
we need to make sure that the browser can establish a client connection
to our WSRPC server endpoint on localhost.
To do that, we need a Certificate Authority to be installed for:
localhost, 127.0.0.1, and ::1, to cover hostname, ipv4 and ipv6,
This is a bit dangerous, in rpc_server we check the Origin header.

We make use of 'mkcert' to generate these certificates and install the root CA.
This can is done locally, on a user level.
Each root CA generated by mkcert is UNIQUE PER PC / ACCOUNT.

TODO(@sas): Add HMAC Challenge (or not. It appears that it might not matter.)

Additionally, a thing to keep in mind is that while the browser uses
the OS' CA store, SSL has it's own stores, therefore we need to append the
one generated by mkcert to SSLs stores at runtime.
"""

